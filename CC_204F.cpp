#define Movazed() { \
    int N; long long K; vector<long long> A; \
    cin >> N >> K; A.resize(N); \
    for (int i = 0; i < N; ++i) cin >> A[i]; \
    long long S = 0; \
    for (int i = 0; i + 1 < N; ++i) S += llabs(A[i+1] - A[i]); \
    long long P0 = 2LL * N + A[0] + A[N-1] + S; \
    if (P0 <= K) { cout << 0 << '\n'; continue; } \
    vector<unsigned long long> cnt(N, 0); \
    vector<int> st; \
    for (int i = 0; i < N; ++i) { \
        while (!st.empty() && A[i] > A[st.back()]) { \
            int mid = st.back(); st.pop_back(); \
            if (st.empty()) break; \
            int left = st.back(); \
            int width = i - left - 1; \
            if (width <= 0) continue; \
            long long h = min(A[i], A[left]) - A[mid]; \
            if (h > 0) cnt[width] += (unsigned long long)h; \
        } \
        st.push_back(i); \
    } \
    unsigned long long need = (unsigned long long)((P0 - K + 1) / 2); \
    unsigned long long total_layers = 0; \
    for (int w = 1; w < N; ++w) total_layers += cnt[w]; \
    if (need > total_layers) { cout << -1 << '\n'; continue; } \
    i128 ops = 0; \
    for (int w = 1; w < N && need; ++w) { \
        if (!cnt[w]) continue; \
        unsigned long long take = min(cnt[w], need); \
        ops += (i128)take * w; \
        need -= take; \
    } \
    cout << (long long)ops << '\n'; \
}
