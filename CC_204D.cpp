#define SOLVE() { \
    int Nmax = 0; \
    for (int i = 0; i < T; ++i) { \
        cin >> N[i]; \
        Nmax = max(Nmax, N[i]); \
    } \
    if (Nmax == 0) { \
        while (T--) cout << 0 << '\n'; \
        return 0; \
    } \
    vector<int> inv(Nmax + 1, 1); \
    for (int i = 2; i <= Nmax; ++i) { \
        inv[i] = mulmod(MOD - MOD / i, inv[MOD % i]); \
    } \
    vector<int> e(Nmax + 1, 0); \
    vector<int> P(Nmax + 1, 0); \
    e[0] = 1; \
    int lastComputedE = 0; \
    for (int n = 2; n <= Nmax; n += 2) { \
        for (int t = lastComputedE + 1; t <= n - 1; ++t) { \
            long long s = 0; \
            for (int k = 2; k <= t; k += 2) { \
                s += 1LL * P[k] * e[t - k]; \
                if (s >= (1LL<<62)) s %= MOD; \
            } \
            e[t] = mulmod(s % MOD, inv[t]); \
        } \
        lastComputedE = n - 1; \
        long long sp = 0; \
        for (int j = 1; j <= n - 1; j += 2) { \
            long long term = 1LL * e[j - 1] * inv[j] % MOD; \
            term = term * e[(n - 1) - j] % MOD; \
            sp += term; \
            if (sp >= (1LL<<62)) sp %= MOD; \
        } \
        P[n] = int(sp % MOD); \
        long long s = 0; \
        for (int k = 2; k <= n; k += 2) { \
            s += 1LL * P[k] * e[n - k]; \
            if (s >= (1LL<<62)) s %= MOD; \
        } \
        e[n] = mulmod(s % MOD, inv[n]); \
    } \
    for (int i = 0; i < T; ++i) { \
        int n = N[i]; \
        if (n % 2 == 1) cout << 0 << '\n'; \
        else cout << P[n] << '\n'; \
    } \
}
